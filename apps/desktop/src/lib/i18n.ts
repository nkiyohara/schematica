export const messageCatalogs = {
  en,
  ja,
  es,
  "zh-CN": zhCN,
  ko,
  fr,
  de,
} satisfies Record<Locale, Messages>;

export function normalizeLocale(language: string): Locale {
  const normalized = language.trim().replaceAll("_", "-").toLowerCase();
  const direct = locales.find((locale) => normalized === locale.toLowerCase());
  if (direct) return direct;

  if (
    normalized === "zh" ||
    normalized.startsWith("zh-cn-") ||
    normalized === "zh-sg" ||
    normalized.startsWith("zh-sg-") ||
    normalized.startsWith("zh-hans")
  ) {
    return "zh-CN";
  }
  if (normalized.startsWith("zh-")) return "en";

  const primary = normalized.split("-")[0];
  const primaryMatches: Record<string, Locale> = {
    en: "en",
    ja: "ja",
    es: "es",
    ko: "ko",
    fr: "fr",
    de: "de",
  };

  return primaryMatches[primary] ?? "en";
}

export function resolveLocale(
  preference: LocalePreference,
  systemLanguage = navigator.language,
): Locale {
  if (locales.includes(preference as Locale)) {
    return preference as Locale;
  }

  return normalizeLocale(systemLanguage);
}

export function hasMessageKey(key: string): key is MessageKey {
  return key in en;
}

export function translate(
  locale: Locale,
  key: MessageKey,
  params: Record<string, string | number> = {},
): string {
  return messageCatalogs[locale][key].replace(/\{(\w+)\}/g, (_match, name) =>
    String(params[name] ?? `{${name}}`),
  );
}
